import turtle
turtle.getscreen()
t=turtle.Turtle()

# making hexagon
# Pentagon has 6 sides; each exterior angle is 60° (360° ÷ 6)
t.forward(100)
t.left(60)

t.forward(100)
t.left(60)

t.forward(100)
t.left(60)

t.forward(100)
t.left(60)

t.forward(100)
t.left(60)

t.forward(100)
t.left(60)


turtle.done()