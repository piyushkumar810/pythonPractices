import turtle
turtle.getscreen()
t=turtle.Turtle()

# making heptagon
# Pentagon has 7 sides; each exterior angle is 51° (360° ÷ 7)
t.forward(100)
t.left(51)

t.forward(100)
t.left(51)

t.forward(100)
t.left(51)

t.forward(100)
t.left(51)

t.forward(100)
t.left(51)

t.forward(100)
t.left(51)

t.forward(100)
t.left(51)


turtle.done()