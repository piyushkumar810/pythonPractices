import turtle
# Set up the turtle window
turtle.speed(1)  # Set drawing speed (optional)

# Draw the first side
turtle.forward(100)  # Move forward by 100 units
turtle.left(120)     # Turn 120 degrees to the left

# Draw the second side
turtle.forward(100)
turtle.left(120)

# Draw the third side
turtle.forward(100)
turtle.left(120)

# Complete the drawing
turtle.done()  # done is also used to hold the output screen "to come out from the output screen you have to cut from top right"