import turtle
turtle.getscreen()
t=turtle.Turtle()

# making octagon
# Pentagon has 8 sides; each exterior angle is 45° (360° ÷ 8)
t.forward(100)
t.left(45)

t.forward(100)
t.left(45)

t.forward(100)
t.left(45)

t.forward(100)
t.left(45)

t.forward(100)
t.left(45)

t.forward(100)
t.left(45)

t.forward(100)
t.left(45)

t.forward(100)
t.left(45)


turtle.done()